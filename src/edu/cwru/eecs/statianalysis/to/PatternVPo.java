package edu.cwru.eecs.statianalysis.to;

public class PatternVPo {

	private int patternKey;
	private int highlightedVertices;
	private int totalVertices;
	private int highlightedVerticesChosen;

	public int getPatternKey() {
		return patternKey;
	}

	public void setPatternKey(int patternKey) {
		this.patternKey = patternKey;
	}

	public int getHighlightedVertices() {
		return highlightedVertices;
	}

	public void setHighlightedVertices(int highlightedVertices) {
		this.highlightedVertices = highlightedVertices;
	}

	public int getTotalVertices() {
		return totalVertices;
	}

	public void setTotalVertices(int totalVertices) {
		this.totalVertices = totalVertices;
	}

	public int getHighlightedVerticesChosen() {
		return highlightedVerticesChosen;
	}

	public void setHighlightedVerticesChosen(int highlightedVerticesChosen) {
		this.highlightedVerticesChosen = highlightedVerticesChosen;
	}

}
