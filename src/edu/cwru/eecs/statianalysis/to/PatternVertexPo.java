package edu.cwru.eecs.statianalysis.to;

public class PatternVertexPo {
	private int patternKey;
	private int vertexKey;
	private int vertexLabel;
	private int vertexIdx;

	public int getPatternKey() {
		return patternKey;
	}

	public void setPatternKey(int patternKey) {
		this.patternKey = patternKey;
	}

	public int getVertexKey() {
		return vertexKey;
	}

	public void setVertexKey(int vertexKey) {
		this.vertexKey = vertexKey;
	}

	public int getVertexLabel() {
		return vertexLabel;
	}

	public void setVertexLabel(int vertexLabel) {
		this.vertexLabel = vertexLabel;
	}

	public int getVertexIdx() {
		return vertexIdx;
	}

	public void setVertexIdx(int vertexIdx) {
		this.vertexIdx = vertexIdx;
	}

}
